def __getattr__(name: str):
    if name in _TICK_PULSE_EXPORTS:
        tick_pulse = import_module("oqp.research.tick_pulse")
        value = getattr(tick_pulse, name)
        globals()[name] = value
        return value
    raise AttributeError(f"module {__name__!r} has no attribute {name!r}")

